package com.company;

public class Pessoas {
    String marca;
    String roupa;

    public Pessoas(String marca, String roupa) {
        this.marca = marca;
        this.roupa = roupa;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getRoupa() {
        return roupa;
    }

    public void setRoupa(String roupa) {
        this.roupa = roupa;
    }
}
